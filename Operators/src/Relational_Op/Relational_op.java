package Relational_Op;

public class Relational_op {

	public static void main(String[] args) {
		
		int jay = 24;
		int viru = 25;
		
		System.out.println("1. Jay age is greater than viru : ");
		System.out.println(jay > viru);
		
		System.out.println("2. Jay age eqauls to equal viru : ");
		System.out.println(jay == viru);
		
		System.out.println("3. Jay age is not equals to viru : ");
		System.out.println(jay != viru);
		
		System.out.println("4. Jay age is less than viru : ");
		System.out.println(jay < viru);
		
		System.out.println("5. Jay age is less than or eqauls to viru : ");
		System.out.println(jay <= viru);
		
		System.out.println("6. Jay age is Positive no : ");
		System.out.println(jay > 0);
	}
}
